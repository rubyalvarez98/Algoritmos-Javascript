// Leer un número entre el 1 y el 10 e imprimir su equivalente en números romanos

const num = 5;

switch(num) {
case 1: console.log("El numero romano de 1 es: I"); break;
case 2: console.log("El numero romano de 2 es: II"); break;
case 3: console.log("El numero romano de 3 es: III"); break;
case 4: console.log("El numero romano de 4 es: IV"); break;
case 5: console.log("El numero romano de 5 es: V"); break;
case 6: console.log("El numero romano de 6 es: VI"); break;
case 7: console.log("El numero romano de 7 es: VII"); break;
case 8: console.log("El numero romano de 8 es: VIII"); break;
case 9: console.log("El numero romano de 9 es: IX"); break;
case 10: console.log("El numero romano de 10 es: X"); break;
default: console.log("El numero ingresado es invalido"); break;
}